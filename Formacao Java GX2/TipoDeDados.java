public class TipoDeDados {

    public static void main(String[] args) {
        
        byte inteiro1 = 10;
        short inteiro2 = 10;
        int inteiro3 = 10;
        long inteiro4 = 10L;
        
        long soma = inteiro1 + inteiro2 + inteiro3 + inteiro4;

        System.out.println("O valor da soma: " + soma);
    }
}