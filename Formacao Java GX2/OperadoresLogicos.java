public class OperadoresLogicos {
    public static void main(String[] args) {
        
        int i = 78;
        int j = 21;
        int k = 38;
        int x = 78;

        System.out.println("Verificação i > j " + (i > j));
        System.out.println("Verificacao k < j :" + (k < j));
        System.out.println("Verificacao k == j :" + (k == j));
        System.out.println("Verificacao x != i :" + (x != i));
        System.out.println("Verificacao x <= i :" + (x <= i));
        System.out.println("Verificacao x < i :" + (x < i));
    }
}